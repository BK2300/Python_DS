# Loops = There is "For loop" & "While loop"
#         For loops: are
#         While loops: an execute a set of statements as long as a condition is true.
#         Since a For loops runs infinitly, they need to be asked to stop with a "break command".
#           Compared to a while that already been told a breakpoint


    ##while loops
i = 1
while i <= 6:
  print(i)
  i += 1

# example from lecture shown on the a presentation:
counter = 1
while counter <= 10:
    print(counter)
    counter += 1

# adding a break point. So its stops earlier
i = 1
while i < 6:
    print(i)
    if (i == 4):
        break
    i += 1

# adding an Else statement
i = 1
while i < 6:
    print(i)
    i += 1
else:
    print("i is no longer less than 6")

